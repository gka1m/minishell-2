#include "../../inc/minishell.h"

int	b_unset(t_env *env, char **args)
{
	int	i;

	i = 1;
	if (!args[1])
		return (0);
	while (args[i])
	{
		delete_env_var(&env, args[i]);
		i++;
	}
	return (0);
}
